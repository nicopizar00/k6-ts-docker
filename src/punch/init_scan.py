def _names(key: str) -> dict[str, str]:
    return {
        "builder": f"{key}-builder",
        "governance_reviewer": f"{key}-governance",
        "document_architect": f"{key}-document-architect",
        "graphify_adapter": f"{key}-graphify-adapter",
    }


def _now() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def render_init_wave_scan(r: ScanResult) -> str:
    counts: dict[str, int] = {}
    for a in r.assets:
        counts[a.asset_type] = counts.get(a.asset_type, 0) + 1
    surface = ", ".join(f"{k}: {v}" for k, v in sorted(counts.items())) or "none"
    blocking = "\n".join(f"- ❌ {n}" for n in r.notes if "confirm with a human" not in n) or "- ✅ none"
    nonblock = "\n".join(
        f"- ⚠️ {p} lifecycle phase has no mapped asset" for p in r.missing_phases
    ) or "- ✅ none"

    lines = [
        "# INIT_WAVE_SCAN", "",
        GENERATED_BANNER, "",
        f"_Scan time: {_now()}_", "",
        "## Summary", "",
        f"- **Template origin:** {TEMPLATE_ORIGIN} (origin only — not this repo's identity)",
        f"- **Detected project identity (governance key):** `{r.identity_key}`",
        f"- **Key source:** {r.identity_source}"
        + ("  ⚠️ needs human confirmation" if r.identity_needs_confirmation else ""),
        f"- **Existing AI / Copilot surface:** {surface}",
        f"- **Documentation files seen:** {sum(len(v) for v in r.doc_groups.values())}",
        f"- **Doc/tracking reconciliation candidates:** {len(r.debt)}",
        f"- **Output location:** `{r.output_dir_rel}/` ({r.output_dir_reason})",
        "",
        "## Readiness score", "",
        "| Category | Level |", "|---|---|",
    ]
    for k, v in r.readiness.items():
        mark = {"present": "✅", "partial": "⚠️", "missing": "❌"}[v]
        lines.append(f"| {k} | {mark} {v} |")
    lines += [
        "",
        f"**Overall status: `{r.overall_status}`**", "",
        "## Blocking issues", "", blocking, "",
        "## Non-blocking recommendations", "", nonblock, "",
        "## Suggested next command", "",
        "```bash",
        "./bin/punch init --write      # persist these bootstrap maps",
        "# then, once adopted:",
        "/document                     # real documentation reconciliation (Punch Document)",
        "```", "",
        "See `next-actions.md` for the full adoption path.", "",
    ]
    return "\n".join(lines) + "\n"


def render_copilot_assets_map(r: ScanResult) -> str:
    lines = [
        "# copilot-assets-map", "", GENERATED_BANNER, "",
        "First-wave inventory of GitHub Copilot assets. **No files were modified.**",
        "This is a bootstrap proposal — promote into "
        "`docs/ai/governance/ai-assets-registry.md` via `/document` or a governance "
        "adoption step; it is **not** canonical yet.", "",
        "## Inventory", "",
        "| Path | Type | Phase | FM | →Punch? | Graphify? | Status | Action |",
        "|---|---|---|---|---|---|---|---|",
    ]
    if not r.assets:
        lines.append("| _(none detected)_ | | | | | | | |")
    for a in sorted(r.assets, key=lambda x: (x.asset_type, x.path)):
        lines.append(
            f"| `{a.path}` | {a.asset_type} | {a.phase} | "
            f"{'yes' if a.has_frontmatter else 'no'} | "
            f"{'yes' if a.refs_punch else 'no'} | "
            f"{'yes' if a.refs_graphify else 'no'} | {a.status} | {a.action} |"
        )

    lines += ["", "## Suggested lifecycle mapping", "",
              "| Existing | Lifecycle | Type | Action |", "|---|---|---|---|"]
    if not r.lifecycle_map:
        lines.append("| _(no phase-tagged assets)_ | | | |")
    for m in r.lifecycle_map:
        lines.append(f"| `{m['existing']}` | `{m['lifecycle']}` | {m['type']} | {m['action']} |")

    missing = "\n".join(f"- `{p}`" for p in r.missing_phases) or "- ✅ all core phases mapped"
    dups = [a for a in r.assets if a.status == "duplicate"]
    review = [a for a in r.assets if a.action == "review"]
    lines += [
        "", "## Missing lifecycle assets", "", missing,
        "", "## Conflicting / duplicate assets", "",
        ("\n".join(f"- `{a.path}`" for a in dups) or "- ✅ none detected"),
        "", "## Review before adoption", "",
        ("\n".join(f"- `{a.path}` — {a.status}" for a in review) or "- ✅ none flagged"),
        "",
    ]
    return "\n".join(lines) + "\n"


def _yaml_list(items: list[str], indent: str) -> str:
    if not items:
        return f"{indent}[]"
    return "\n".join(f"{indent}- {i}" for i in items)


