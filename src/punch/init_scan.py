def run(args: argparse.Namespace) -> int:
    repo_root = REPO_ROOT
    if not (repo_root / ".git").exists() and not (repo_root / ".github").exists():
        print("[punch init] error: does not look like a repository root "
              "(no .git or .github).", flush=True)
        return 2

    try:
        result = scan(repo_root, with_graphify=args.with_graphify,
                      output_override=args.output)
    except Exception as e:  # robust: a scan crash is a command error, not a readiness fail
        print(f"[punch init] error during scan: {e}", flush=True)
        return 2

    write = bool(args.write) and not args.dry_run

    print(f"[punch init] template origin : {TEMPLATE_ORIGIN}")
    print(f"[punch init] governance key  : {result.identity_key} "
          f"({result.identity_source})"
          + ("  [needs confirmation]" if result.identity_needs_confirmation else ""))
    print(f"[punch init] overall status  : {result.overall_status}")
    print(f"[punch init] copilot assets  : {len(result.assets)} | "
          f"doc files: {sum(len(v) for v in result.doc_groups.values())} | "
          f"debt candidates: {len(result.debt)} | "
          f"graphify: {'installed' if result.graphify['cli_installed'] else 'absent (optional)'}")
    print(f"[punch init] output location : {result.output_dir_rel}/ "
          f"({result.output_dir_reason})")

    rendered = {name: fn(result) for name, fn in ARTIFACTS.items()}

    if write:
        out_dir = repo_root / result.output_dir_rel
        out_dir.mkdir(parents=True, exist_ok=True)
        for name, content in rendered.items():
            (out_dir / name).write_text(content, encoding="utf-8")
            print(f"[punch init] wrote {result.output_dir_rel}/{name}")
    else:
        print("[punch init] DRY-RUN — no files written. Would generate:")
        for name in rendered:
            print(f"             {result.output_dir_rel}/{name}")
        print("[punch init] re-run with --write to persist these bootstrap maps.")

    if result.overall_status == "not_ready":
        print("[punch init] blocking readiness issues found (exit 1):")
        for n in result.notes:
            print(f"             - {n}")
        return 1
    return 0
